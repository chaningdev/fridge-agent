def get_default_days(name: str) -> int | None:
    """食材名から標準保存日数を返す。マッチしなければ None。"""
    for keywords, days in _SHELF_LIFE:
        if any(kw in name for kw in keywords):
            return days
    return None


def get_default_expiry(name: str) -> str | None:
    """食材名から標準賞味期限の日付文字列（YYYY-MM-DD）を返す。マッチしなければ None。"""
    days = get_default_days(name)
    if days is None:
        return None
    return (date.today() + timedelta(days=days)).isoformat()
